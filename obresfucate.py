reverse_firstu = {v: k for k, v in firstu.items()}
_tokens_desc = sorted(reverse_firstu.keys(), key=len, reverse=True)


def obfuscate(text: str) -> str:
    return "".join(firstu.get(c, c) for c in text)


def deobfuscate(text: str) -> str:
    if not text:
        return text
    out = []
    i = 0
    n = len(text)
    while i < n:
        matched = False
        for token in _tokens_desc:
            if text.startswith(token, i):
                out.append(reverse_firstu[token])
                i += len(token)
                matched = True
                break
        if not matched:
            out.append(text[i])
            i += 1
    return "".join(out)

